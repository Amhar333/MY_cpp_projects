#include <iostream>
using namespace std;

//Compiler version g++ 6.3.0

int main()
{
    double x, y;
    cout << "x, y >" ;
    cin >> x >> y;
    cout << "x = " << x;
    cout << "\ny = " << y;
    cout << "\n\nx + y =" << (x + y) ;
    cout << "\nx - y =" << (x - y) ;
    cout << "\nx * y =" << (x * y) ;
    cout << "\nx / y =" << (x / y);
  return 0;

}
